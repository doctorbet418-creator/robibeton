// ── האם עכשיו סופ"ש? ──
function isWeekend() {
  const now = new Date();
  const day = now.getDay();
  const hour = now.getHours();
  if (day === 4 && hour >= 18) return true;
  if (day === 5 && hour < 17) return true;
  return false;
}

